extern "C" void write_text_to_screen_font9(String text, uint16_t text_color, uint16_t text_bg_color, int16_t x_pos, int16_t y_pos) {
    SCREEN.setCursor(x_pos, y_pos);
    SCREEN.setFreeFont(FM9);
    SCREEN.setTextColor(text_color, text_bg_color);
    SCREEN.println(text);
}

